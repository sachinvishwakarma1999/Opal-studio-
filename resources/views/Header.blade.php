// When the user scrolls down 80px from the top of the document, resize the navbar's padding and the logo's font size
window.onscroll = function() {scrollFunction()};

function scrollFunction() {
  if (document.body.scrollTop > 50 || document.documentElement.scrollTop > 50) {
    document.getElementById("navbar").style.padding = "8px 10px";
    document.getElementById("navbar").style.background = "linear-gradient(100deg, rgb(255, 255, 255) 20%, rgba(255, 255, 255, 0.19) 100%);";
      document.getElementById("img_lg").style.display = "none";
      document.getElementById("icon_scroll").style.display = "none";
      document.getElementById("col_cls").classList.remove("col-lg-4"); 
      document.getElementById("col_cls").classList.add("col-lg-2");
  } else {
    document.getElementById("navbar").style.padding = "8px 10px";
     document.getElementById("navbar").style.background = "linear-gradient(100deg, rgb(255, 255, 255) 20%, rgba(255, 255, 255, 0.19) 100%);";
    document.getElementById("img_lg").style.display = "block";
    document.getElementById("icon_scroll").style.display = "block";
    document.getElementById("col_cls").classList.remove("col-lg-2"); 
      document.getElementById("col_cls").classList.add("col-lg-4");
  }
}
